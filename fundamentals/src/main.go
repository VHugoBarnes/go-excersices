package main

import "fmt"

func main() {

	x := 10
	y := 20

	// Addition
	z := x + y

	// Substraction
	zx := x - y

	// Multiplication
	zi := x * y

	// Division
	zw := x / y

	// Module
	zq := x % y

	// Incremental
	x++

	// Decremental
	x--

	fmt.Println("Addition: ", z)
	fmt.Println("Substraction: ", zx)
	fmt.Println("Multiplication: ", zi)
	fmt.Println("Division: ", zw)
	fmt.Println("Module: ", zq)
	fmt.Println("x: ", x)

}
